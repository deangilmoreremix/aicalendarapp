export const getAvatarByIndex = (index: number, set: keyof typeof avatarSets = 'executives'): string => {
  const avatars = avatarSets[set];
  return avatars[index % avatars.length];
};

export const getRandomAvatar = (set: keyof typeof avatarSets = 'executives'): string => {
  const avatars = avatarSets[set];
  return avatars[Math.floor(Math.random() * avatars.length)];
};